DROP INDEX IF EXISTS idx_tasks_trace_id;
DROP INDEX IF EXISTS idx_evidence_fact_id;
DROP INDEX IF EXISTS idx_facts_subject;
DROP INDEX IF EXISTS idx_chunks_episode_id;
DROP INDEX IF EXISTS idx_events_kind_ts;
DROP INDEX IF EXISTS idx_events_trace_id;

DROP TABLE IF EXISTS outbox;
DROP TRIGGER IF EXISTS events_no_delete;
DROP TRIGGER IF EXISTS events_no_update;
DROP TABLE IF EXISTS events;
DROP TABLE IF EXISTS tasks;
DROP TABLE IF EXISTS merge_ledger;
DROP TABLE IF EXISTS evidence;
DROP TABLE IF EXISTS entity_aliases;
DROP TABLE IF EXISTS entities;
DROP TABLE IF EXISTS facts;
DROP TABLE IF EXISTS chunks;
DROP TABLE IF EXISTS episodes;
